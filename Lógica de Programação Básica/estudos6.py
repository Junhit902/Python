# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro tipo
#  tipos imutáveis e primitivos: str, int, float, bool
# Exemplo: '0' é uma string, pois está dentro de aspas simples
# Se colocarmos assim int('0'), a string é convertido para int

print(int('1'), type(int('1')))
print(type(float('1') + 1))
print(bool(' '))
print(str(11) + 'b')
