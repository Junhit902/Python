adicao = 10 + 10
print('Adição de 10 + 10 =', adicao)

subtracao = 20 - 10
print('Subtração de 20 - 10 =', subtracao)

multiplicacao = 10 * 10
print('Multiplicação de 10 x 10 =', multiplicacao)

divisao = 10 / 5 #Vai retornar sempre um float
print('Divisão de 10 / 5 =', divisao)

divisao_inteira = 10 // 4
print('Divisão inteira de 10 // 4 =', divisao_inteira) #O resultado sempre ignorará as casas decimais, Exemplo: 10 / 4 = 2.5

exponenciacao = 10 ** 2
print('Exponenciação de 10^2 =', exponenciacao)

modulo = 10 % 2
print('Módulo  de 10 / 2 =', modulo) #Resto da divisão

#É par ou ímpar?
x = 10

par_impar = (x % 2 == 0) # Se o resto da divisão por 2 for 0 é par, senão é ímpar
print('O número 10 é par?', par_impar)  #Se for True é par e se for False é ímpar